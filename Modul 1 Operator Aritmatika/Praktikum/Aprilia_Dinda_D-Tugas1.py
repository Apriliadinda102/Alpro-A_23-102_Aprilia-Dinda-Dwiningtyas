#konversi suhu
c = int(input("Masukkan Suhu Celcius"))

#Menghitung Celcius ke Farenheit
f = 9/5*c+32
print("Hasil konversi suhu dari celcius ke farenheit adalah ", f, "Farenheit") 

#Menghitung Celcius ke Reamur
r = 4/5*c
print("Hasil konversi suhu dari celcius ke reamur adalah", r, "Reamur")

#Menghitung Celcius ke Kelvin
k = c+273
print("Hasil konversi suhu dari celcius ke kelvin adalah", k, "Kelvin")
